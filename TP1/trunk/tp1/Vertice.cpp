// Vertice.cpp: implementation of the Vertice class.
//
//////////////////////////////////////////////////////////////////////

#include "Vertice.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

Vertice::Vertice(int coordX, int coordY)
{
	x = coordX;
	y = coordY;
}

Vertice::Vertice()
{
	x = 0;
	y = 0;
}

Vertice::~Vertice()
{

}

//////////////////////////////////////////////////////////////////////
// Getters y Setters
//////////////////////////////////////////////////////////////////////

int Vertice::getX()
{
	return x;
}

int Vertice::getY()
{
	return y;
}

Vertice::setX(int coordX)
{
	x = coordX;
}

Vertice::setY(int coordY)
{
	y = coordY;
}



//////////////////////////////////////////////////////////////////////
// Otros métodos
//////////////////////////////////////////////////////////////////////
Vertice::dibujar()
{
	glVertex2i(x,y);
}

